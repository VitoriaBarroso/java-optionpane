class funcaoValue
{
    public static void main (String args[])
    {
        int a = 11;
        long b = 222;
        float c = 3333;
        double d = 4.444;
        String S = String.valueOf(a) + " * " + String.valueOf(b) + " * " +
                String.valueOf(c) + " * " + String.valueOf(d);
        System.out.println("Conteudo de S: " + S);
    }
}
